import 'dart:io';

import 'package:flutter_test/flutter_test.dart';
import 'package:integration_test/integration_test.dart';
import 'package:path/path.dart' as p;
import 'package:photo_review_app/src/rust/api/library.dart' as rust;
import 'package:photo_review_app/src/rust/frb_generated.dart';
import 'package:flutter_rust_bridge/flutter_rust_bridge_for_generated.dart'
    show Int64List;

void main() {
  IntegrationTestWidgetsFlutterBinding.ensureInitialized();
  setUpAll(() async => await RustLib.init());

  testWidgets('full flow: index → preprocess → decide → export',
      (tester) async {
    final tmp = await Directory.systemTemp.createTemp('prv_test');
    addTearDown(() => tmp.delete(recursive: true));

    await rust.initDb(dbPath: p.join(tmp.path, 'library.db'));

    // Fake card: 4 real JPEGs (ffmpeg-generated fixtures, distinct content).
    // No EXIF -> mtime fallback; equal mtimes group them into one burst.
    final card = Directory(p.join(tmp.path, 'card', 'DCIM'))
      ..createSync(recursive: true);
    final now = DateTime.now();
    for (var i = 0; i < 4; i++) {
      // cwd is the project root when run via `flutter test -d macos`;
      // fall back to an embedded tiny JPEG if the fixtures aren't found.
      final fixture = File('integration_test/fixtures/frame${i + 1}.jpg');
      final bytes =
          fixture.existsSync() ? fixture.readAsBytesSync() : _tinyJpeg();
      final f = File(p.join(card.path, 'DSC0000$i.JPG'));
      f.writeAsBytesSync(bytes);
      f.setLastModifiedSync(now);
    }

    final source = await rust.selectSource(rootPath: p.join(tmp.path, 'card'));
    final cache = p.join(tmp.path, 'cache');
    final output = p.join(tmp.path, 'output');
    await rust.setCacheFolder(sourceId: source.id, path: cache);
    await rust.setOutputFolder(sourceId: source.id, path: output);

    // Index.
    final events = await rust.startIndex(sourceId: source.id).toList();
    expect(events.last.finished, isTrue);
    expect(events.last.photos, BigInt.from(4));
    expect(events.last.bursts, BigInt.one);

    // Preprocess: stills + burst MP4.
    final prep = await rust.startPreprocess(sourceId: source.id).toList();
    expect(prep.last.finished, isTrue);
    expect(prep.last.failures, isEmpty);
    final status = await rust.getCacheStatus(sourceId: source.id);
    expect(status.stillsCached, status.stillsTotal);
    expect(status.videosCached, BigInt.one);

    // Decide: keep frame 2, keep the video, mark burst done.
    final bursts = await rust.listBursts(sourceId: source.id, offset: 0, limit: 10);
    expect(bursts, hasLength(1));
    expect(bursts.first.videoCachePath, isNotNull);
    final frames = await rust.getBurstFrames(
        sourceId: source.id, burstId: bursts.first.id);
    expect(frames, hasLength(4));
    expect(frames[2].previewPath, isNotNull, reason: 'cached still exists');
    await rust.setFrameKeep(
        photoIds: Int64List.fromList([frames[2].id]), keep: true);
    await rust.setKeepVideo(burstId: bursts.first.id, keep: true);
    await rust.setBurstStatus(burstId: bursts.first.id, status: 'done');

    // Export.
    final plan = await rust.planExport(sourceId: source.id);
    expect(plan.keeperPhotos, BigInt.one);
    expect(plan.videos, BigInt.one);
    final export = await rust.startExport(sourceId: source.id).toList();
    expect(export.last.finished, isTrue);
    expect(export.last.failures, isEmpty);
    expect(export.last.filesCopied, BigInt.one);
    expect(export.last.videosCopied, BigInt.one);

    final keeper = File(p.join(output, 'keepers', 'DSC00002.JPG'));
    expect(keeper.existsSync(), isTrue);
    expect(
      Directory(p.join(output, 'videos'))
          .listSync()
          .whereType<File>()
          .where((f) => f.path.endsWith('.mp4')),
      hasLength(1),
    );

    // Re-export: idempotent.
    final again = await rust.startExport(sourceId: source.id).toList();
    expect(again.last.filesSkipped, BigInt.one);
    expect(again.last.videosSkipped, BigInt.one);
    expect(again.last.filesCopied, BigInt.zero);
  });
}

